# Dictionary in python
# Dictionary is a collection of key-value pairs
# Creating a dictionary
dictionary = {"name": "John", "age": 30, "city": "New York", "is_student": False}
print(dictionary)
print(dictionary["name"])  # Accessing value by key
print(dictionary.keys())  # Getting all keys
print(dictionary.values())  # Getting all values
print(dictionary.items())  # Getting all key-value pairs
dictionary["age"] = 31  # Updating a value
print(dictionary)
